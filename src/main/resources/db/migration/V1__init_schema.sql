
CREATE TABLE Адрес (
    Код_адреса SERIAL PRIMARY KEY,
    Область VARCHAR(100) NOT NULL,
    Район VARCHAR(100),
    Город VARCHAR(100) NOT NULL,
    Улица VARCHAR(100) NOT NULL,
    Дом VARCHAR(10) NOT NULL,
    Помещение VARCHAR(20)
);

CREATE TABLE Паспорт (
    Код_паспорта SERIAL PRIMARY KEY,
    Серия_паспорта VARCHAR(4) NOT NULL CHECK (LENGTH(Серия_паспорта) = 4),
    Номер_паспорта VARCHAR(6) NOT NULL CHECK (LENGTH(Номер_паспорта) = 6),
    Дата_выдачи DATE NOT NULL,
    Кем_выдан VARCHAR(200) NOT NULL
);

CREATE TABLE Должность (
    Код_должности SERIAL PRIMARY KEY,
    Название VARCHAR(50) NOT NULL UNIQUE,
    Оклад DECIMAL(10,2) NOT NULL CHECK (Оклад > 0)
);

CREATE TABLE Направление (
    Код_направления SERIAL PRIMARY KEY,
    Название VARCHAR(50) NOT NULL UNIQUE,
    Тип_отдыха VARCHAR(50)
);

CREATE TABLE Туроператор (
    Код_оператора SERIAL PRIMARY KEY,
    Название_компании VARCHAR(100) NOT NULL UNIQUE,
    ИНН VARCHAR(12) NOT NULL UNIQUE,
    Код_адреса INT NOT NULL UNIQUE,
    Контактное_лицо VARCHAR(100),
    Телефон VARCHAR(12) CHECK (LENGTH(Телефон) = 12),
    Email VARCHAR(100) UNIQUE,
    Рейтинг DECIMAL(3,2) CHECK (Рейтинг BETWEEN 0 AND 10),
    FOREIGN KEY (Код_адреса) REFERENCES Адрес(Код_адреса)
);

CREATE TABLE Сотрудник (
    Код_сотрудника SERIAL PRIMARY KEY,
    ФИО VARCHAR(100) NOT NULL,
    Дата_приема DATE NOT NULL,
    Телефон VARCHAR(12) CHECK (LENGTH(Телефон) = 12),
    Email VARCHAR(100) UNIQUE,
    Логин VARCHAR(50) NOT NULL UNIQUE,
    Пароль VARCHAR(100) NOT NULL CHECK (LENGTH(Пароль) >= 8),
    Код_должности INT NOT NULL,
    Код_адреса INT NOT NULL,
    FOREIGN KEY (Код_должности) REFERENCES Должность(Код_должности),
    FOREIGN KEY (Код_адреса) REFERENCES Адрес(Код_адреса)
);

CREATE TABLE Клиент (
    Код_клиента SERIAL PRIMARY KEY,
    ФИО VARCHAR(100) NOT NULL,
    Дата_рождения DATE,
    Код_паспорта INT NOT NULL,
    Номер_телефона VARCHAR(12) NOT NULL CHECK (LENGTH(Номер_телефона) = 12),
    Email VARCHAR(100) UNIQUE,
    Код_адреса INT NOT NULL,
    FOREIGN KEY (Код_паспорта) REFERENCES Паспорт(Код_паспорта),
    FOREIGN KEY (Код_адреса) REFERENCES Адрес(Код_адреса)
);

CREATE TABLE Тур (
    Код_тура SERIAL PRIMARY KEY,
    Название VARCHAR(150) NOT NULL,
    Код_направления INT NOT NULL,
    Код_оператора INT NOT NULL,
    Страна VARCHAR(50) NOT NULL,
    Город VARCHAR(50),
    Отель VARCHAR(100),
    Дата_начала DATE NOT NULL,
    Дата_окончания DATE NOT NULL CHECK (Дата_окончания > Дата_начала),
    Стоимость DECIMAL(10,2) NOT NULL CHECK (Стоимость > 0),
    Кол_во_мест INT NOT NULL CHECK (Кол_во_мест >= 0),
    Тип_питания VARCHAR(50),
    Описание TEXT,
    FOREIGN KEY (Код_направления) REFERENCES Направление(Код_направления),
    FOREIGN KEY (Код_оператора) REFERENCES Туроператор(Код_оператора)
);

CREATE TABLE Бронирование (
    Код_бронирования SERIAL PRIMARY KEY,
    Код_клиента INT NOT NULL,
    Код_тура INT NOT NULL,
    Код_сотрудника INT NOT NULL,
    Дата_бронирования DATE NOT NULL DEFAULT CURRENT_DATE,
    Дата_оплаты DATE,
    Статус VARCHAR(20) NOT NULL DEFAULT 'Забронировано' CHECK (Статус IN ('Забронировано','Оплачено','Отменено','Завершён')),
    Итоговая_сумма DECIMAL(12,2) CHECK (Итоговая_сумма >= 0),
    Номер_договора VARCHAR(20) NOT NULL UNIQUE,
    FOREIGN KEY (Код_клиента) REFERENCES Клиент(Код_клиента) ON DELETE CASCADE,
    FOREIGN KEY (Код_тура) REFERENCES Тур(Код_тура) ON DELETE CASCADE,
    FOREIGN KEY (Код_сотрудника) REFERENCES Сотрудник(Код_сотрудника)
);

CREATE TABLE ПредпочтенияКлиента (
    Код_предпочтения SERIAL PRIMARY KEY,
    Код_клиента INT NOT NULL,
    Код_направления INT NOT NULL,
    FOREIGN KEY (Код_клиента) REFERENCES Клиент(Код_клиента) ON DELETE CASCADE,
    FOREIGN KEY (Код_направления) REFERENCES Направление(Код_направления) ON DELETE CASCADE,
    UNIQUE(Код_клиента, Код_направления)
);

